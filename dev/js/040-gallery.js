(function(App) {

	var module = {};

	//------------------------------------------------------------------------------------------------------------------------------------------------------------
	// module init method
	//------------------------------------------------------------------------------------------------------------------------------------------------------------
	module.init = function() {
		App.debugging( 'Initiate gallery', 'report' );


		$('.js-gallery').nivoSlider({
			pauseTime: 5000,
			directionNav: false,
			controlNav: false,
		});
	};


	App.gallery = module;

}(App));