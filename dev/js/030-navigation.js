/***************************************************************************************************************************************************************
 *
 * Navigation module
 *
 * Takes care of the off-canvas menu.
 *
 **************************************************************************************************************************************************************/


(function(App) {

	var module = {};

	//------------------------------------------------------------------------------------------------------------------------------------------------------------
	// module init method
	//------------------------------------------------------------------------------------------------------------------------------------------------------------
	module.init = function() {
		App.debugging( 'Initiate nav', 'report' );


		$('.js-nav').on('click', function() {
			App.debugging( 'Nav menu button clicked', 'interaction' );

			$('.js-body').toggleClass('is-openmenu');

		});
	};


	App.nav = module;

}(App));